<hr>
<h4>Especificações e Características</h4>
<p>Desenvolvimento de um CRUD de Usuário (<i><u>matricula</u>, nome, data de nascimento, e-mail, senha</i>).
<br>Listagem de Usuários com busca e filtros oferecendo opções de Edição e Exclusão.
<br>Matricula gerada automaticamente utilizando ano, mês e número sequencial de 4 dígitos. Exemplo: 2016030022
<br>Edição de usuário trazendo suas informações.
<br>Persistência de senha utilizando função de Hash SHA512.
<br>Linguagem server-side: PHP
<br>Banco de Dados: MySQL
</p><br>

<h4>Interface</h4>
<p>Para o desenvolvimento da interface foi utilizado HTML, CSS, <a href="http://getbootstrap.com/" target="_blank">Bootstrap Framework</a> e JavaScript.</p><br>

